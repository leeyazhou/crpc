package com.github.leeyazhou.crpc.core.annotation;

import java.lang.annotation.Documented;
import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

import com.github.leeyazhou.crpc.core.object.SideType;

/**
 * @author lee
 */
@Documented
@Retention(RetentionPolicy.RUNTIME)
@Target(value = { ElementType.TYPE })
public @interface CRPCFilterType {
  String name() default "";

  SideType[] active() default { SideType.SIDE_PROVIDER, SideType.SIDE_CONSUMER };
}
